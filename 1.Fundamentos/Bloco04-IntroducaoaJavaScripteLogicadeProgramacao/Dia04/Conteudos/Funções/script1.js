function soma(a, b) {
  let resultado = a + b;
  return "Soma: " + resultado 
}

function subtracao(a, b) {
  let resultado = a - b;
  return "Subtração: " + resultado;
}

function multiplicacao(a, b) {
  let resultado = a * b;
  return "Multiplicação: " + resultado;
}

function divisao(a, b) {
  let resultado = a / b;
  return "Divisão: " + resultado;
}

function modulo(a, b) {
  let resultado = a % b;
  return "Módulo: " + resultado;
}

console.log(soma(5, 7));
console.log(subtracao(5, 7));
console.log(multiplicacao(5, 7));
console.log(divisao(5, 7));
console.log(modulo(5, 7));
